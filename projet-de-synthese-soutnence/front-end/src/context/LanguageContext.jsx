export const LanguageProvider = ({ children }) => {
  const [language, setLanguage] = useState(() => {
    return localStorage.getItem('app_language') || 'darija';
  });

  useEffect(() => {
    localStorage.setItem('app_language', language);
    // Dynamically adjust HTML document dir attribute
    document.documentElement.dir = (language === 'en' || language === 'fr') ? 'ltr' : 'rtl';
    document.documentElement.lang = language === 'darija' ? 'ar' : language;
  }, [language]);

  const t = (key) => {
    return translations[language]?.[key] || translations['darija']?.[key] || key;
  };

  return (
    <LanguageContext.Provider value={{ language, changeLanguage: setLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  );
};

export const useLanguage = () => {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
};
